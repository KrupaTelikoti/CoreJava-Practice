package com.dl.two;

//operator overloading

public class Parent {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		System.out.println(a+b);
		
		String s1 = "10";
		System.out.println(s1);
		String s2 = "20";
		System.out.println(s2);
		System.out.println(s1+s2);
		

	}

}

